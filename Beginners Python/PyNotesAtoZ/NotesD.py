# list in python 😁
planet = [ 'marz', 'mercurry' , 'zupiter' , 'SatEarn' , 'YourAnus' , 'NapTune' , 'BlueToe']
print(planet)
print(planet[0])
print(planet[-5])
print(planet[5][1]) #letter 1 of index number 5
print(planet[2:]) #everything from index number 2
print(planet[2:6]) #everything from index number 2 to before 6
print(planet[-7:3])

