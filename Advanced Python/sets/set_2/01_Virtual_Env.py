
# Installing virtualenv:
#  pip install virtualenv

# Test your installation:
#  virtualenv --version

# Using virtualenv:
# You can create a virtualenv using the following command:
#  virtualenv my_env

# After running this command, a directory named my_name will be created. This is the directory which contains all the necessary 
# executables to use the packages that a Python project would need. This is where Python packages will be installed.

# Now after creating virtual environment, you need to activate it. Remember to activate the relevant virtual environment every time you work on the project. 
# This can be done using the following command:
#  .\my_env\Scripts\activate.ps1
